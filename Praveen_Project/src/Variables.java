
public class Variables
{
	public static void main(String args[])
	{
		int k = 10; // 4 bytes-> 32 bits
	    short m = 10; // 2 bytes-> 16 bits -> -32768 to 32767
	    byte n = 10; // 1 byte-> 8 bits ->-128 to 127
	    long o = 500000000000l; //8 bytes
	    float p = 5.2f;
	    double d = 5.2;
	    
	    
	    char c = 'K';
	    c = 66; // ASCII value of 66
	    System.out.println(c);
	    
	    double d1 = 5; //implicit conversion
	    int i = (int)5.5; //type casting
	    System.out.print(i);
	  
	    
	}

}
